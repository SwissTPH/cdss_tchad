function ageToMini(y){

    if (y >= 182){
        return ( y - ((y - 182) % 30) + 15)
    }else if (y >= 60){
        return (y - ((y - 60) % 7) + 3)
    }else {
        return y;
    }
}

function computeZScore(y, m ,l,s){

    return ((Math.pow((y / m), l) - 1) / (s * l));

}

function computeReverseZScore(z, m ,l,s){
    return (m * Math.pow((z*s*l+1),(1/l)));
}

